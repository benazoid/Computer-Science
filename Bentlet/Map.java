    public GameObject[][] getObjs(int l){
        GameObject[][] go = new GameObject[32][32];
        int[][] level = getLevel(l);
        
        for(int i = 0; i < level.length; i++){
            for(int j = 0; j < level[i].length; i++){
                int num = level[i][j];
                GameObject obj = items[((num/10)-(int)Math.floor(num/10))*10];
                if(obj instanceof Moveable){
                    moves.add(obj);
                }
                else{
                    go[i][j] = obj;
//                    go[i][j].setLoc(new Point(i,j));
                }
            }
        }
        return go;
    }

            
